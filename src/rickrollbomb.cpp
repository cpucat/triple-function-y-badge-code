// sizeof gives the number of bytes, each int value is composed of two bytes (16 bits)
// there are two values per note (pitch and duration), so for each note there are four bytes
int bombnotes = sizeof(bombmelody) / sizeof(bombmelody[0]) / 2;

// this calculates the duration of a whole note in ms
int bombwholenote = (60000 * 4) / bombtempo;

int bombdivider = 0, bombnoteDuration = 0;

void playBombSong() {
  // iterate over the notes of the melody.
  // Remember, the array is twice the number of notes (notes + durations)
  for (int thisNote = 0; thisNote < bombnotes * 2; thisNote = thisNote + 2) {

    // calculates the duration of each note
    bombdivider = bombmelody[thisNote + 1];
    if (bombdivider > 0) {
      // regular note, just proceed
      bombnoteDuration = (bombwholenote) / bombdivider;
    } else if (bombdivider < 0) {
      // dotted notes are represented with negative durations!!
      bombnoteDuration = (bombwholenote) / abs(bombdivider);
      bombnoteDuration *= 1.5; // increases the duration in half for dotted notes
    }

    // we only play the note for 90% of the duration, leaving 10% as a pause
    speaker_play_note(bombmelody[thisNote], bombnoteDuration * 0.9);

    // Wait for the specief duration before playing the next note.
    delay(bombnoteDuration);
  }
}

void rickBomb()
{
    //wait for button to release
    while(buttons_get(1) || buttons_get(2) || buttons_get(3) || switches_get(1) || switches_get(2))
    {
      delay(1);
      for (int i=0; i<=20; i++)
      {
        leds_set_color(i, 255, 0, 0);
      }
    }

    //set countdown
    int delay_time = knob_get() + 1;

    delay_time *= 500;

    
    for (int i=0; i<=20; i++)
    {
      leds_set_color(i, 100, 100, 100);
    }

    //wait
    for (int i=20; i>=0; i--)
    {
      delay(delay_time);
      leds_set_color(i, 0, 0, 0);
    }

    playBombSong();
}